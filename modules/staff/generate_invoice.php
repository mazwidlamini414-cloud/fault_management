// ── Handle: Generate Invoice ──────────────────────────────────────────────────
if ($_SERVER['REQUEST_METHOD'] === 'POST' && ($_POST['action'] ?? '') === 'generate') {
    $invoice_id = (int)($_POST['invoice_id'] ?? 0);
    $due_date   = mysqli_real_escape_string($conn, $_POST['due_date'] ?? '');
    $notes      = mysqli_real_escape_string($conn, $_POST['notes'] ?? '');

    $chk = mysqli_query($conn, "
        SELECT i.*, c.COMPANY_NAME, c.COMPANY_EMAIL, c.CLIENT_ID,
               rf.REP_FAULT_ID, rf.STATUS as FAULT_STATUS
        FROM invoice i
        LEFT JOIN client c ON i.CLIENT_ID = c.CLIENT_ID
        LEFT JOIN assignment a ON i.ASSIGN_ID = a.ASSIGN_ID
        LEFT JOIN reported_fault rf ON a.REP_FAULT_ID = rf.REP_FAULT_ID
        WHERE i.INVOICE_ID = $invoice_id AND i.TYPE = 'Quotation'");

    if (mysqli_num_rows($chk) === 0) {
        $error_msg = 'Invalid quotation selected.';
    } elseif (empty($due_date)) {
        $error_msg = 'Please set a payment due date.';
    } else {
        $quot  = mysqli_fetch_assoc($chk);
        $today = date('Y-m-d');

        $upd = mysqli_query($conn, "
            UPDATE invoice SET
                TYPE         = 'Invoice',
                STATUS       = 'Pending Payment',
                INVOICE_DATE = '$today',
                DUE_DATE     = '$due_date'
            WHERE INVOICE_ID = $invoice_id
        ");

        if ($upd) {
            $inv_ref   = 'INV-' . str_pad($invoice_id, 4, '0', STR_PAD_LEFT);
            $total_fmt = number_format($quot['TOTAL'], 2);
            $client_id = (int)$quot['CLIENT_ID'];

            // Audit trail
            mysqli_query($conn, "
                INSERT INTO invoice_tracking (invoice_id, action, description, performed_by_id, performed_by_role)
                VALUES ($invoice_id, 'Invoice Generated',
                    'Invoice $inv_ref generated by accountant. Due: $due_date. Notes: $notes',
                    $acc_id, 'Accountant')
            ");

            // Notify client
            mysqli_query($conn, "
                INSERT INTO notifications (user_id, user_type, title, message)
                VALUES ($client_id, 'Client',
                    'Invoice $inv_ref Ready for Payment',
                    'Your invoice $inv_ref for E$total_fmt has been generated. Payment is due by $due_date. Please log in to make payment.')
            ");

            // Notify admin
            mysqli_query($conn, "
                INSERT INTO notifications (user_id, user_type, title, message)
                VALUES (1, 'Admin',
                    'Invoice $inv_ref Generated',
                    'Accountant generated invoice $inv_ref (E$total_fmt) for client. Due: $due_date.')
            ");

            $success_msg = "Invoice $inv_ref generated and sent to client successfully!";
        } else {
            $error_msg = 'Database error: ' . mysqli_error($conn);
        }
    }
}

// ── Handle: Re-send Invoice Notification ──────────────────────────────────────
if ($_SERVER['REQUEST_METHOD'] === 'POST' && ($_POST['action'] ?? '') === 'resend') {
    $invoice_id = (int)($_POST['invoice_id'] ?? 0);

    $inv = mysqli_query($conn, "
        SELECT i.*, c.COMPANY_NAME, c.CLIENT_ID
        FROM invoice i
        LEFT JOIN client c ON i.CLIENT_ID = c.CLIENT_ID
        WHERE i.INVOICE_ID = $invoice_id AND i.TYPE = 'Invoice'");

    if (mysqli_num_rows($inv) > 0) {
        $row       = mysqli_fetch_assoc($inv);
        $client_id = (int)$row['CLIENT_ID'];
        $inv_ref   = 'INV-' . str_pad($invoice_id, 4, '0', STR_PAD_LEFT);
        $total_fmt = number_format($row['TOTAL'], 2);
        $due       = $row['DUE_DATE'];

        mysqli_query($conn, "
            INSERT INTO notifications (user_id, user_type, title, message)
            VALUES ($client_id, 'Client',
                'Reminder: Invoice $inv_ref Awaiting Payment',
                'This is a reminder that invoice $inv_ref for E$total_fmt is due on $due. Please log in to complete payment.')
        ");

        mysqli_query($conn, "
            INSERT INTO invoice_tracking (invoice_id, action, description, performed_by_id, performed_by_role)
            VALUES ($invoice_id, 'Payment Reminder Sent',
                'Payment reminder sent to client by accountant.',
                $acc_id, 'Accountant')
        ");

        $success_msg = "Payment reminder for $inv_ref sent to client.";
    } else {
        $error_msg = 'Invoice not found.';
    }
}

