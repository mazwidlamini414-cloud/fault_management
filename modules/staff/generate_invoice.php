        c.COMPANY_NAME, c.COMPANY_EMAIL, c.CLIENT_ID, c.CONTACT_PERSON_NAME,
        rf.REP_FAULT_ID, rf.STATUS as FAULT_STATUS, rf.PRIORITY, rf.REPORT_DATE,
        rf.DESCRIPTION as FAULT_DESC,
        a.ASSIGN_ID, a.ASSIGN_DATE,
        e.FULL_NAME as TECH_NAME,
        cc.confirmed_at as CLIENT_APPROVED_AT,
        cc.confirmation_notes
    FROM invoice i
    INNER JOIN assignment a       ON i.ASSIGN_ID    = a.ASSIGN_ID
    INNER JOIN reported_fault rf  ON a.REP_FAULT_ID = rf.REP_FAULT_ID
    LEFT JOIN  client c           ON i.CLIENT_ID    = c.CLIENT_ID
    LEFT JOIN  assignment_technician at2 ON a.ASSIGN_ID = at2.ASSIGN_ID
    LEFT JOIN  employee e         ON at2.EMP_ID     = e.EMP_ID
    LEFT JOIN  client_confirmations cc ON cc.fault_id = rf.REP_FAULT_ID
    WHERE i.TYPE   = 'Quotation'
      AND rf.STATUS = 'Client Approved'
    GROUP BY i.INVOICE_ID
    ORDER BY cc.confirmed_at DESC, i.INVOICE_DATE DESC
");

