// ── Card toggle ────────────────────────────────────────────────────────────────
function toggleCard(id) {
    const body = document.getElementById('acb-' + id);
    const chev = document.getElementById('chev-' + id);
    const card = document.getElementById('ac-' + id);
    const isOpen = body.classList.contains('open');

    // Close all others
    document.querySelectorAll('.ac-body').forEach(b => b.classList.remove('open'));
    document.querySelectorAll('.approved-card').forEach(c => c.classList.remove('selected'));
    document.querySelectorAll('[id^="chev-"]').forEach(c => { c.className = 'ti ti-chevron-down'; });

    if (!isOpen) {
        body.classList.add('open');
        card.classList.add('selected');
        chev.className = 'ti ti-chevron-up';
        card.scrollIntoView({ behavior: 'smooth', block: 'nearest' });
    }
}

// ── Invoice Preview Modal ──────────────────────────────────────────────────────
function openPreview(invoiceId, company, total, contact, faultId) {
    document.getElementById('pv-inv-num').textContent  = 'INV-' + String(invoiceId).padStart(4, '0');
    document.getElementById('pv-client').textContent   = company;
    document.getElementById('pv-contact').textContent  = contact || '—';
    document.getElementById('pv-fault').textContent    = '#' + faultId;
    document.getElementById('pv-total').textContent    = 'E' + parseFloat(total).toFixed(2);

    // Get due date from the card's form if set
    const dueInput = document.querySelector('#acb-' + invoiceId + ' input[name="due_date"]');
    if (dueInput && dueInput.value) {
        const d = new Date(dueInput.value);
        document.getElementById('pv-due').textContent = d.toLocaleDateString('en-GB', {day:'2-digit',month:'short',year:'numeric'});
    }

    // Build lines table from DOM
    const lineTable = document.querySelector('#acb-' + invoiceId + ' .line-mini');
    const pvLines   = document.getElementById('pv-lines');
    if (lineTable) {
        pvLines.innerHTML = lineTable.outerHTML;
        pvLines.querySelector('table').style.width = '100%';
    } else {
        pvLines.innerHTML = '<p style="color:var(--text2);font-size:.8rem;text-align:center">No line items available.</p>';
    }

    document.getElementById('preview-modal').classList.add('open');
    document.body.style.overflow = 'hidden';
}

function closePreview() {
    document.getElementById('preview-modal').classList.remove('open');
    document.body.style.overflow = '';
}

// Close modal on backdrop click
document.getElementById('preview-modal').addEventListener('click', function(e) {
    if (e.target === this) closePreview();
});

// ESC key
document.addEventListener('keydown', function(e) {
    if (e.key === 'Escape') closePreview();
});

// ── Auto-open card if there's only one ────────────────────────────────────────
document.addEventListener('DOMContentLoaded', function() {
    const cards = document.querySelectorAll('.approved-card');
    if (cards.length === 1) {
        const id = cards[0].id.replace('ac-', '');
        toggleCard(parseInt(id));
    }
});

